package com.gongpingjia.carplay.po;

public class UserInfo {

	private String userId;

	private String nickname;

	private String gender;

	private Integer age;

	private String photo;

	private String carBrandLogo;

	private String carModel;

	private String drivingLicensePhoto;

	private Integer drivingExperience;

	private String province;

	private String city;

	private String district;

	private Byte isauthenticated;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getCarBrandLogo() {
		return carBrandLogo;
	}

	public void setCarBrandLogo(String carBrandLogo) {
		this.carBrandLogo = carBrandLogo;
	}

	public String getCarModel() {
		return carModel;
	}

	public void setCarModel(String carModel) {
		this.carModel = carModel;
	}

	public Integer getDrivingExperience() {
		return drivingExperience;
	}

	public void setDrivingExperience(Integer drivingExperience) {
		this.drivingExperience = drivingExperience;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public Byte getIsauthenticated() {
		return isauthenticated;
	}

	public void setIsauthenticated(Byte isauthenticated) {
		this.isauthenticated = isauthenticated;
	}

	public String getDrivingLicensePhoto() {
		return drivingLicensePhoto;
	}

	public void setDrivingLicensePhoto(String drivingLicensePhoto) {
		this.drivingLicensePhoto = drivingLicensePhoto;
	}

}
